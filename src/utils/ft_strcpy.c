#include "../../inc/minishell.h"

char	*ft_strcpy(char *dst, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[j])
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	return (dst);
}