#include "../../inc/minishell.h"
/**
 * @brief Convert the string in int
 * 
 * @param str String to convert
 * @return int 
 */
int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	s;
	size_t	num;

	i = 0;
	num = 0;
	s = 1;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			s *= -1;
			i++;
		}
		else
			i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		num = num * 10 + (str[i++] - '0');
	s *= num;
	return (s);
}
